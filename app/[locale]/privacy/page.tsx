export async function generateMetadata({
  params
}: {
  params: Promise<{locale: Locale}>;
}): Promise<Metadata> {
  const {locale} = await params;
  const text = content[locale];

  return {
    title: `${text.title} | LONGQING`,
    description: text.description,
    alternates: {
      canonical: `/${locale}/privacy`,
      languages: {
        ru: "/ru/privacy",
        zh: "/zh/privacy",
        en: "/en/privacy",
        "x-default": "/en/privacy"
      }
    }
  };
}

export default async function PrivacyPage({
  params
}: {
  params: Promise<{locale: Locale}>;
}) {
  const {locale} = await params;
  setRequestLocale(locale);
  const text = content[locale];

  return (
    <main className="bg-light px-5 pb-20 pt-32 text-dark">
      <article className="mx-auto max-w-4xl">
        <h1 className="text-3xl font-semibold leading-tight md:text-5xl">{text.title}</h1>
        <div className="mt-6 rounded-2xl border border-slate-200 bg-white p-5 text-sm leading-7 text-neutral-700 shadow-sm">
          {text.meta.map((item) => (
            <p key={item}>{item}</p>
          ))}
        </div>
        <p className="mt-8 text-base leading-8 text-neutral-700">{text.description}</p>
        <div className="mt-10 grid gap-9">
          {text.sections.map((section) => (
            <section key={section.title}>
              <h2 className="text-2xl font-semibold">{section.title}</h2>
              <div className="mt-3 grid gap-3 text-base leading-8 text-neutral-700">
                {section.blocks.map((block, index) =>
                  block.type === "paragraph" ? (
                    <p key={`${section.title}-${index}`}>{block.text}</p>
                  ) : (
                    <ul key={`${section.title}-${index}`} className="list-disc space-y-2 pl-6">
                      {block.items.map((item) => (
                        <li key={item}>{item}</li>
                      ))}
                    </ul>
                  )
                )}
              </div>
            </section>
          ))}
        </div>
      </article>
    </main>
  );
}
